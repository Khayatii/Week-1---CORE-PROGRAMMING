public class HarryAge {
    public static void main(String[] args) {
        // Harry's Birth Year is - 2000
        // Saving it in a variable
        // Saving the current year in a variable

        int birthYear = 2000, currentYear = 2024;

        // age can be calculated by subtracting birthYear from currentYear

        int age = currentYear - birthYear;

        // Printing out Harry's age
        System.out.println("Harry's age in 2024 is : " + age);

    }
}